									<article>
										 
										<p><strong>Background</strong></p>
<p>The session will witness the launch of a joint effort between WHO and the International Telecommunication Union (ITU) and the signing of a “Letter of Intent” under the auspices of African leaders, together with ministers of health and ICT, to leverage strategic opportunities for the adoption of ICT in the health sector. The initiative will aim, through cross-sectoral collaboration, to establish foundations to enable large-scale eHealth programmes and ensure the needed capacity at all levels of the health system.</p>
<p>ICT is already transforming how health care is delivered and how health systems are run. For ICT to achieve health goals and to ensure healthy lives and promote well-being for all for the African region, the partnership will consolidate existing efforts and resources towards making available ICT foundations and platforms that are a requirement for providing and scaling up eHealth services. It will also focus on building a capable workforce to effectively use ICT. The partnership will address also the need of multi-stakeholders partnership models that can bring about sustainable adoption of Digital Health.</p>
