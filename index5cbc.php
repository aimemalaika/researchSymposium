        <p><p>Dr Matshidiso Moeti from Botswana is the first woman WHO Regional Director for Africa. She aims to build a responsive, effective and result-driven regional secretariat that can advance efforts towards universal health coverage and accelerate progress toward global development goals, while tackling emerging threats. Strong partnerships will underpin every aspect of the Regional Office&#8217;s work during her tenure.</p>
<p>Dr Moeti is a public health veteran, with more than 35 years of national and international experience. She joined WHO’s Africa Regional Office in 1999 and has served as Deputy Regional Director, Assistant Regional Director, Director of Noncommunicable Diseases, WHO Representative for Malawi, and Coordinator of the Inter-Country Support Team for the South and East African countries.</p>
