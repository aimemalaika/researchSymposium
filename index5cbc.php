<p>Dr Moeti qualified in medicine (M.B., B.S) and public health (MSc in Community Health for Developing Countries) at the Royal Free Hospital School of Medicine, University of London in 1978 and the London School of Hygiene and Tropical Medicine in 1986, respectively.</p></p>
